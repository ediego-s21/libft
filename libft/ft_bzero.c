#include "libft.h"

void ft_bzero(void *s, size_t n)
{
	unsigned int i;
	unsigned char *ss;

	i = 0;
	ss = (unsigned char *)s;
	while (i < n)
	{
		ss[i] = '\0';
		i++;
	}
}
